const char *CErrCodeMap::GetErrDescrStr(const char *pszErrorCode)
{
    if (nullptr == pszErrorCode)
    {
        return "";
    }
    if (strlen(pszErrorCode) != 8)
    {
        return "";
    }

    ERRMAP::iterator it = m_errmap.find(pszErrorCode);
    if (it == m_errmap.end())
    {
        return "";
    }

    const char  *szDescr = nullptr;//应该返回的最恰当的对象
    int    iNumberCount = 0;//找到的目标的数字个数
    int    iMaxCount = 0;  //找到目标的最大数字个数

    for (; it != m_errmap.end() && !m_errmap.key_comp()(pszErrorCode, it->first)
         && !m_errmap.key_comp()(it->first, pszErrorCode); it++)
    {
        iNumberCount = 0;
        for (int i = 0; i < 8; i++)//统计数字个数（0~9  或 A~F）
        {
            if ((('0' <= it->first[i]) && ('9' >= it->first[i]))   ||
                (('A' <= it->first[i]) && ('F' >= it->first[i])))
            {
                iNumberCount++;
            }
        }

        if (iMaxCount < iNumberCount)
        {
            iMaxCount = iNumberCount;
            szDescr = it->second; //保存找到的目标
        }
    }// end for 匹配的目标项

    if (nullptr  !=  szDescr)
    {
        return  szDescr;
    }

    return "";
}

void CErrCodeMap::InsertToMap(const char *szErr, const char *szDescr)
{
    m_errmap.insert(pair<const char *, const char *>(szErr, szDescr));
}
